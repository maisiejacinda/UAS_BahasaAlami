"""# SOAL 4
## Named Entity Recognition (NER)

### 4.1 Pembentukan Dataset BIO Tagging

Tahap ini membentuk dataset berformat BIO (Begin, Inside, Outside) dengan memberikan label pada setiap token berdasarkan aturan (rule-based). Dataset BIO yang dihasilkan akan digunakan sebagai data latih pada proses Named Entity Recognition (NER).
"""

import pandas as pd
import os

print("🔄 Memulai proses konversi kalimat ke format Token-Level BIO Tagging...")

# Menentukan kumpulan kata kunci (Lexicon-Rule) yang didefinisikan sebagai entitas ASPECT
# Ini selaras dengan masalah teknis yang sering muncul di ulasan TikTok-mu
kata_kunci_aspek = {
    "masuk": "ASPECT",
    "daftar": "ASPECT",
    "bug": "ASPECT",
    "update": "ASPECT",
    "perbarui": "ASPECT",
    "jeda": "ASPECT",
    "live": "ASPECT",
    "berat": "ASPECT"
}

baris_bio_token = []

# Mengambil 100 sampel kalimat teratas (memenuhi syarat minimal 100-200 kalimat NER di soal)
sampel_kalimat_ner = df['Clean_Normalized_Text'].head(100).tolist()

for indeks_kalimat, kalimat in enumerate(sampel_kalimat_ner):
    if not isinstance(kalimat, str) or kalimat.strip() == "":
        continue

    # Memecah kalimat menjadi token kata secara runtut
    daftar_token = kalimat.split()

    for i, token in enumerate(daftar_token):
        label_tag = "O"  # Default tag adalah Outside (Bukan entitas)

        # Aturan Pencocokan Aturan (Rule-Based Matching):
        if token in kata_kunci_aspek:
            # Periksa apakah token sebelumnya juga entitas yang sama untuk menentukan B- atau I-
            if i > 0 and daftar_token[i-1] in kata_kunci_aspek:
                label_tag = "I-ASPECT" # Inside Aspect (Lanjutan entitas)
            else:
                label_tag = "B-ASPECT" # Begin Aspect (Awal entitas)

        # Menyimpan data token level beserta indeks kalimatnya
        baris_bio_token.append({
            "Sentence_Idx": f"Sentence_{indeks_kalimat}",
            "Token": token,
            "Tag": label_tag
        })

# Mengubah hasil penandaan menjadi DataFrame
