"""### 4.2 Pelatihan Model CRF

Pada tahap ini dilakukan ekstraksi fitur setiap token, pembagian data menjadi data latih dan data uji, pelatihan model Conditional Random Field (CRF), serta penyimpanan model hasil pelatihan dalam format `.joblib` untuk digunakan kembali pada aplikasi.
"""

def token2features(sent, i):
    word = sent[i]
    features = {
        'bias': 1.0,
        'word.lower()': word.lower(),
        'word[-3:]': word[-3:],
        'word[-2:]': word[-2:],
        'word.isdigit()': word.isdigit(),
    }
    if i > 0:
        word1 = sent[i-1]
        features.update({
            '-1:word.lower()': word1.lower(),
        })
    else:
        features['BOS'] = True

    if i < len(sent) - 1:
        word1 = sent[i+1]
        features.update({
            '+1:word.lower()': word1.lower(),
        })
    else:
        features['EOS'] = True

    return features

def sent2features(sent):
    return [token2features(sent, i) for i in range(len(sent))]

df_bio = pd.read_csv('data/ner_bio_dataset.tsv', sep='\t')

# Kelompokkan token dan tag (menggunakan include_groups=False untuk mematikan DeprecationWarning jika pakai Pandas v2.x)
agg_func = lambda s: [(w, t) for w, t in zip(s["Token"].astype(str).tolist(), s["Tag"].astype(str).tolist())]

try:
    grouped_sentences = df_bio.groupby("Sentence_Idx", group_keys=False).apply(agg_func).tolist()
except TypeError:
    grouped_sentences = df_bio.groupby("Sentence_Idx").apply(agg_func).tolist()

X = [sent2features([token[0] for token in kalimat]) for kalimat in grouped_sentences]
