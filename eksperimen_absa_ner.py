import numpy as np


def hitung_minimum_edit_distance(kata_asal, kata_tujuan):
    """
    Menghitung jumlah operasi minimal (insert, delete, substitute)
    untuk mengubah kata_asal menjadi kata_tujuan (Soal 02 Langkah 4).
    """
    m = len(kata_asal)
    n = len(kata_tujuan)

    matriks_dp = [[0] * (n + 1) for _ in range(m + 1)]


    for i in range(m + 1):
        matriks_dp[i][0] = i
    for j in range(n + 1):
        matriks_dp[0][j] = j

    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if kata_asal[i-1] == kata_tujuan[j-1]:
                matriks_dp[i][j] = matriks_dp[i-1][j-1]
            else:
                matriks_dp[i][j] = 1 + min(
                    matriks_dp[i-1][j],
                    matriks_dp[i][j-1],
                    matriks_dp[i-1][j-1]
                )
    return matriks_dp[m][n]



def hitung_cosine_similarity_manual(vektor_a, vektor_b):
    """
    Menghitung kemiripan sudut arah antara dua vektor teks numerik.
    """
    perkalian_titik = np.dot(vektor_a, vektor_b)
    norma_a = np.linalg.norm(vektor_a)
    norma_b = np.linalg.norm(vektor_b)

    if norma_a == 0 or norma_b == 0:
        return 0.0
    return perkalian_titik / (norma_a * norma_b)


