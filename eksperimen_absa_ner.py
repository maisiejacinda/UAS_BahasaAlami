import re

def pipeline_cleaning_regex(text):
    """
    Fungsi untuk melakukan Case Folding dan pembersihan teks menggunakan Regex.
    Tahapan ini memenuhi instruksi SOAL 02 komponen kelengkapan preprocessing.
    """
    if not isinstance(text, str):
        return ""

    text_lowercase = text.lower()

    text_clean_noise = re.sub(r'https?://\S+|www\.\S+', '', text_lowercase)
    text_clean_noise = re.sub(r'@[^\s]+', '', text_clean_noise)
    text_clean_noise = re.sub(r'[^a-zA-Z\s]', '', text_clean_noise)


    text_normalized_space = re.sub(r'\s+', ' ', text_clean_noise).strip()

    return text_normalized_space

